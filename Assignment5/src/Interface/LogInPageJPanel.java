    private void btnSubmitLoginPageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitLoginPageActionPerformed
        // TODO add your handling code here:
        String userNameInput = txtUserNameLP.getText();
        String passwordInput = txtPasswordLP.getText();
        
        boolean found = false;
        for (Account a : userAccountDirectory.getAccountList()) {
            if (a.getUserName().equals(userNameInput) && a.getPassword().equals(passwordInput) && a.getStatus().equalsIgnoreCase("active")) {
                String roleReturned = a.getRole();
                if (roleReturned.equalsIgnoreCase("System")) {
                    SystemAdminMainJPanel systemAdminMainJPanel = new SystemAdminMainJPanel(userProcessContainer, userAccountDirectory, a.getPerson(),personDirectory);
                    userProcessContainer.add("SystemAdminMainJPanel", systemAdminMainJPanel);
                    found = true;
                } else if (roleReturned.equalsIgnoreCase("HR")) {
                    HRMainJPanel hrMainJPanel = new HRMainJPanel(userProcessContainer, personDirectory, a.getPerson(),userAccountDirectory);
                    userProcessContainer.add("SystemAdminMainJPanel", hrMainJPanel);
                    found = true;
                }
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }    
                
        }
        if(!found)
            JOptionPane.showMessageDialog(null, "The username and password does not exist or the account is inactive");
        
    }//GEN-LAST:event_btnSubmitLoginPageActionPerformed


