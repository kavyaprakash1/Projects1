    private void BtnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnViewDetailsActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblVitalSigns.getSelectedRow();
        
        if(selectedRow >=0)
        {
          VitalSigns vs = (VitalSigns)tblVitalSigns.getValueAt(selectedRow,0);
          vTxtTemperature.setText(String.valueOf(vs.getTemperature()));
          vTextPulse.setText(String.valueOf(vs.getPulse()));
          vTxtBloodPressure.setText(String.valueOf(vs.getBloodPressure()));
          vTxtDate.setText(vs.getDate());
          
        }
        else
            JOptionPane.showMessageDialog(null,"Select a row to be displayed");
        
    }//GEN-LAST:event_BtnViewDetailsActionPerformed

    private void btnDeleteDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteDetailsActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblVitalSigns.getSelectedRow();
        
        if(selectedRow >= 0)
        {
            VitalSigns vs = (VitalSigns)tblVitalSigns.getValueAt(selectedRow,0); 
            vsh.deleteVital(vs);
            JOptionPane.showMessageDialog(null,"Vital Sign has been deleted");
            populateTable();
            
            vTxtTemperature.setText("");
            vTextPulse.setText("");
            vTxtBloodPressure.setText("");
            vTxtDate.setText("");
           
          
        }
        else 
            JOptionPane.showMessageDialog(null,"Select atleast one item");
    }//GEN-LAST:event_btnDeleteDetailsActionPerformed


