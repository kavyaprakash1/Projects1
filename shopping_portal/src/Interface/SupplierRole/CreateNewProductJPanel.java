    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        if ((txtName.getText().equals("")) || (txtAvailability.getText().equals(""))) {
            JOptionPane.showMessageDialog(null, "Please enter both fields");
            return;
        }
        String prodName = txtName.getText();
        int availibility = 0;
        try{
             availibility = Integer.parseInt(txtAvailability.getText());
        }
        catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "Please enter both fields");
            return;
        }

        Product product = new Product();
        product.setProdName(prodName);
        product.setAvailability(availibility);

        supplier.getProductCatalog().getProductList().add(product);

        JOptionPane.showMessageDialog(null, "Product added!", "Info", JOptionPane.INFORMATION_MESSAGE);
        txtName.setText("");
        txtAvailability.setText("");
    }//GEN-LAST:event_btnAddActionPerformed

